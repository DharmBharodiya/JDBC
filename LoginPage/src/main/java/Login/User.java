package Login;

public class User {
	String name;
	String department;
	String email;
	String mobile;
	String password;
	
	public User(String name, String department, String email, String mobile, String password){
		this.name = name;
		this.department = department;
		this.email = email;
		this.mobile = mobile;
		this.password = password;
	}
	
}
